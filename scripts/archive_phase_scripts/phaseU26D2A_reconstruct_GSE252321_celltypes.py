#!/usr/bin/env python3
"""
Phase U26D2A
Balanced marker-based cell-type reconstruction for GSE252321.

Inputs
------
Four sparse cells-by-genes integer-count matrices generated by U26D1A v1.1.

Workflow
--------
1. Harmonize the common mouse gene universe across all four samples.
2. Retain adaptive-QC-passing cells.
3. Draw a balanced integration subset from every biological sample.
4. Library-size normalize and log1p transform.
5. Select highly variable genes without densifying the full matrix.
6. Run truncated SVD, sample-centroid correction and MiniBatchKMeans.
7. Annotate clusters with curated marker-panel scores.
8. Project all QC-passing cells onto the reconstruction.
9. Quantify sample-by-cell-type composition and targeted Treg/CD137L signals.
10. Produce annotation diagnostics for review before pseudobulk testing.

Biological samples remain the inferential units. This phase does not perform
cell-level significance testing and does not modify the manuscript or existing
Figures 1-6.
"""

